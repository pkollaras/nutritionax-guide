export const getTranslation = (lang: Language, key: string): any => {
  const keys = key.split('.');
  let result: any = translations[lang];
  
  for (const k of keys) {
    result = result?.[k];
    if (result === undefined) {
      console.warn(`Translation key not found: ${key} for language: ${lang}`);
      return key;
    }
  }
  
  return result;
};

export const getDayTranslation = (lang: Language, englishDay: string): string => {
  const dayMap: Record<string, string> = {
    'Monday': getTranslation(lang, 'days.monday'),
    'Tuesday': getTranslation(lang, 'days.tuesday'),
    'Wednesday': getTranslation(lang, 'days.wednesday'),
    'Thursday': getTranslation(lang, 'days.thursday'),
    'Friday': getTranslation(lang, 'days.friday'),
    'Saturday': getTranslation(lang, 'days.saturday'),
    'Sunday': getTranslation(lang, 'days.sunday'),
  };
  
  return dayMap[englishDay] || englishDay;
};
